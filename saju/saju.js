function searchDream(q) {
  q = (q || '').trim().toLowerCase();
  if (!q) return DREAM_DB;
  return DREAM_DB.filter(item =>
    item.t.toLowerCase().includes(q) ||
    item.m.toLowerCase().includes(q) ||
    item.k.some(k => k.toLowerCase().includes(q) || q.includes(k.toLowerCase()))
  );
}

