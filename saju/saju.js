// 시지(時支) 경계: [시작시, 지지index]
function hourToJi(hour) {
  if (hour >= 23 || hour < 1)  return 0;  // 자시 23:00–00:59
  return Math.floor((hour + 1) / 2) % 12; // 축01, 인03, …
}

