// 정오 기준 율리우스적일(JDN)
function toJDN(y, m, d) {
  const a = Math.floor((14 - m) / 12);
  const yy = y + 4800 - a;
  const mm = m + 12 * a - 3;
  return d + Math.floor((153 * mm + 2) / 5) + 365 * yy
    + Math.floor(yy / 4) - Math.floor(yy / 100) + Math.floor(yy / 400) - 32045;
}

// 월지 결정(절기 근사)
function getMonthJi(month, day) {
  // 1월은 소한(1/6) 이전이면 전년 12월지(자), 이후면 축
  let ji = 1; // 기본 축
  for (let i = 0; i < SOLAR_TERMS.length; i++) {
    const t = SOLAR_TERMS[i];
    if (month === t.m && day >= t.d) ji = t.ji;
  }
  // 각 달의 "경계 이전" 처리: 해당 달 경계일 이전이면 직전 절기의 월지
  const cur = SOLAR_TERMS.find(t => t.m === month);
  if (cur && day < cur.d) {
    const idx = SOLAR_TERMS.indexOf(cur);
    ji = SOLAR_TERMS[(idx - 1 + 12) % 12].ji;
  } else if (cur) {
    ji = cur.ji;
  } else {
    ji = 1;
  }
  return ji;
}

/* -------------------- 핵심: 사주팔자 계산 -------------------- */
function computeSaju(y, m, d, hour /* number 0-23 or null */) {
  // 1) 년주 — 입춘(2/4) 이전 출생은 전년으로
  let yearForPillar = y;
  if (m < 2 || (m === 2 && d < 4)) yearForPillar = y - 1;
  const yGan = (yearForPillar - 4) % 10;
  const yJi  = (yearForPillar - 4) % 12;

  // 2) 월주 — 월지(절기) + 오호둔(년간→월간)
  const mJi = getMonthJi(m, d);
  // 인월(寅, ji=2)을 기준으로 몇 번째 월인가
  const monthOrder = (mJi - 2 + 12) % 12; // 인=0, 묘=1 …
  // 오호둔: 갑기년 병인월 시작 → 인월 월간 = (년간%5)*2 + 2
  const monthStemStart = ((yGan % 5) * 2 + 2) % 10;
  const mGan = (monthStemStart + monthOrder) % 10;

  // 3) 일주 — JDN 기준
  const jdn = toJDN(y, m, d);
  const dayIndex = ((jdn + DAY_GANJI_OFFSET) % 60 + 60) % 60;
  const dGan = dayIndex % 10;
  const dJi  = dayIndex % 12;

  // 4) 시주 — 오서둔(일간→자시간)
  let hourPillar = null;
  if (hour !== null && hour !== undefined && !Number.isNaN(hour)) {
    const hJi = hourToJi(hour);
    const hourStemStart = ((dGan % 5) * 2) % 10; // 갑기일→갑자시
    const hGan = (hourStemStart + hJi) % 10;
    hourPillar = { gan: hGan, ji: hJi };
  }

  const pillars = {
    year:  { gan: yGan, ji: yJi },
    month: { gan: mGan, ji: mJi },
    day:   { gan: dGan, ji: dJi },
    hour:  hourPillar
  };

  // 오행 집계
  const count = { 목:0, 화:0, 토:0, 금:0, 수:0 };
  ['year','month','day','hour'].forEach(k => {
    const p = pillars[k];
    if (!p) return;
    count[GAN_OHAENG[p.gan]]++;
    count[JI_OHAENG[p.ji]]++;
  });

  return { pillars, count, animal: JI_ANIMAL[yJi], dayMaster: dGan };
}

